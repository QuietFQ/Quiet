package com.yubo.play;

public class Person {

	private int age;
	private String username;
	private Object wrife;
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public Object getWrife() {
		return wrife;
	}
	public void setWrife(Object wrife) {
		this.wrife = wrife;
	}
}
